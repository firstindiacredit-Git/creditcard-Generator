const CardSearch = () => {
  const [cardType, setCardType] = useState('americanexpress');
  const [numOfCards, setNumOfCards] = useState('');
  const [filteredCards, setFilteredCards] = useState([]);

  const shuffleArray = (array) => {
    let shuffledArray = [...array];
    for (let i = shuffledArray.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [shuffledArray[i], shuffledArray[j]] = [shuffledArray[j], shuffledArray[i]];
    }
    return shuffledArray;
  };

  const handleSearch = (e) => {
    e.preventDefault();
    const numCards = Math.min(numOfCards, 150);
    let cardData = [];

    if (cardType === 'americanexpress') {
      cardData = americanexpress;
    } else if (cardType === 'mastercard') {
      cardData = mastercard;
    } else if (cardType === 'visa') {
      cardData = visa;
    } else if (cardType === 'dinersclub') {
      cardData = dinersclub;
    } else if (cardType === 'discover') {
      cardData = discover;
    }

    const shuffledData = shuffleArray(cardData);
    const filtered = shuffledData.slice(0, numCards);
    setFilteredCards(filtered);
  };

  return (
    <div className="">
      <div style={{backgroundColor:"black" , marginTop:"-1.4rem", marginBottom:"2rem", color:"white", padding:"0.3rem"}}>
        <h1 className="">Generate Credit Cards</h1>
      </div>
      <form onSubmit={handleSearch} className="card-form">
        <div className="form-group">
          <label className="form-label">Select Card Type</label>
          <select
            value={cardType}
            onChange={(e) => setCardType(e.target.value)}
            className="form-select"
          >
            <option value="americanexpress">American Express</option>
            <option value="mastercard">MasterCard</option>
            <option value="visa">Visa</option>
            <option value="discover">Discover</option>
            <option value="dinersclub">Diners Club</option>
          </select>
        </div>

        <div className="form-group">
          <label className="form-label">Number of Cards (Max 150)</label>
          <input
            type="number"
            value={numOfCards}
            onChange={(e) => setNumOfCards(e.target.value)}
            className="form-input"
            placeholder="Enter number of cards"
            max="150"
            required
          />
        </div>

        <button type="submit" className="form-button">Search</button>
      </form>


      {filteredCards.length > 0 && (
        <div className="card-grid">
          {filteredCards.map((card, index) => (
            <div key={index} className="card-item">
              {cardType === 'mastercard' && <MasterCardCard card={card} />}
              {cardType === 'americanexpress' && <AmericanCardCard card={card} />}
              {cardType === 'visa' && <VisaCardCard card={card} />}
              {cardType === 'discover' && <DiscoverCardCard card={card} />}
              {cardType === 'dinersclub' && <DinersCardCard card={card} />}
            </div>
          ))}
        </div>
      )}
    </div>
  );
};

export default CardSearch;
